#include <unistd.h>
#include "../includes/malloc.h"

void	ft_putendl(char const *s)
{
	int i;

	i = 0;
	if (s)
	{
		while (s[i])
		{
			i++;
		}
		write(1, s, i);
		ft_putchar('\n');
	}
}
